    //método para abrir el fichero
    public void rellenaAnimals() {
        try {
            FileInputStream fis = new FileInputStream("Animals.dat");//variable para el archivo
            ObjectInputStream inputFile = new ObjectInputStream(fis);//creamos inputFile para meter datos en el fichero
            boolean finalFichero = false;
            while (!finalFichero) {
                try {
                    cuidados.add((Cuidados) inputFile.readObject());

                } catch (EOFException e) {//excepción para final de fichero y con el boolean hacemos que salga del buche
                    finalFichero = true;
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(addCuidados.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            fis.close();
            inputFile.close();

        } catch (IOException f) {
            JOptionPane.showMessageDialog(null, f.getMessage());

        }

    }

    public void saveCare() {

        try {
            FileOutputStream fos = new FileOutputStream("Animals.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(fos);
            for (int i = 0; i < cuidados.size(); i++) {
                outputFile.writeObject(cuidados.get(i));
            }
            outputFile.close();
            fos.close();
            JOptionPane.showMessageDialog(null, "Guardado Correctamente");
            this.dispose();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(addCuidados.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(addCuidados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

