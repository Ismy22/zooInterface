    public void newCare() {

        //variables para guardar los datos de los textfields      
        String tipoComida = jtTipoComida.getText();

        String habitat = jtHabitat.getText();

        float costeProm = Float.parseFloat(jtCostePromedio.getText());

        float cantidadComida = Float.parseFloat(jtCantidaComida.getText());

        int pericidadComida = Integer.parseInt(jtPeriocidadComida.getText());

        Cuidados a = new Cuidados(tipoComida, habitat, costeProm, cantidadComida, pericidadComida); //creamos un paciente nuevo con los datos de las variables

        //lo guardamos en una arraylist
        cuidados.add(a);
        saveCare();//guardado automático

    }

